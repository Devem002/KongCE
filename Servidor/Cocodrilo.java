package Servidor;


// Clase que representa un cocodrilo
public class Cocodrilo {

    int liana;
    int altura;
    boolean esRojo;
    boolean moviendose; 

    public Cocodrilo(int liana, int altura, boolean esRojo) {
        this.liana = liana;
        this.altura = altura;
        this.esRojo = esRojo;
        this.moviendose = false;
    }
    
}
